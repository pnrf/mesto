/** Создайте класс Section, который отвечает за отрисовку элементов на странице. Этот класс:
 * Первым параметром конструктора принимает объект с двумя свойствами: items и renderer. Свойство items — это массив данных,
 * которые нужно добавить на страницу при инициализации класса. Свойство renderer — это функция, которая отвечает за создание и отрисовку данных на странице.
 * Второй параметр конструктора — селектор контейнера, в который нужно добавлять созданные элементы.
 * Содержит публичный метод, который отвечает за отрисовку всех элементов. Отрисовка каждого отдельного элемента должна осуществляться функцией renderer.
 * Содержит публичный метод addItem, который принимает DOM-элемент и добавляет его в контейнер.
 * У класса Section нет своей разметки. Он получает разметку через функцию-колбэк и вставляет её в контейнер.
*/

//import { cardListSelector } from "../utils/constants";

export default class Section {
  constructor({items, renderer}, cardListSelector) {
    this._items = items;
    this._renderer = renderer;
    this._containerElements = document.querySelector(cardListSelector);
  }

  /** renderItems -- публичный метод: отрисовать все элементы на странице.
   * На вход принимает массив данных.
   * Содержит функцию renderer, которая перебирает вх. массив данных и отрисовывает каждый элемент в отдельности.
  */
  renderItems() {
    this._items.forEach(item => {
      this._renderer(item);
    });
  }

  /** addItem -- публичный метод.
   * На вход принимает DOM-элемент и добавляет его в контейнер (вначало методом append).
   */
  addItem(item) {
    this._containerElements.append(item);
  }
}

